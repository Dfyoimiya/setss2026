-- -----------------------------------------------------------------------
-- 9. 验证数据完整性（行数统计）
-- -----------------------------------------------------------------------
DO $$
DECLARE
    cnt_users         INT;
    cnt_papers        INT;
    cnt_paper_authors INT;
    cnt_reviews       INT;
    cnt_registrations INT;
    cnt_announcements INT;
    cnt_configs       INT;
BEGIN
    SELECT COUNT(*) INTO cnt_users         FROM users;
    SELECT COUNT(*) INTO cnt_papers        FROM papers;
    SELECT COUNT(*) INTO cnt_paper_authors FROM paper_authors;
    SELECT COUNT(*) INTO cnt_reviews       FROM reviews;
    SELECT COUNT(*) INTO cnt_registrations FROM registrations;
    SELECT COUNT(*) INTO cnt_announcements FROM announcements;
    SELECT COUNT(*) INTO cnt_configs       FROM system_configs;

    RAISE NOTICE '=== SETSS 2026 Seed Data Summary ===';
    RAISE NOTICE 'users:         %', cnt_users;
    RAISE NOTICE 'papers:        %', cnt_papers;
    RAISE NOTICE 'paper_authors: %', cnt_paper_authors;
    RAISE NOTICE 'reviews:       %', cnt_reviews;
    RAISE NOTICE 'registrations: %', cnt_registrations;
    RAISE NOTICE 'announcements: %', cnt_announcements;
    RAISE NOTICE 'system_configs:%', cnt_configs;

    -- 断言外键完整性
    ASSERT cnt_users >= 8,         'Expected at least 8 users';
    ASSERT cnt_papers >= 5,        'Expected at least 5 papers';
    ASSERT cnt_reviews >= 6,       'Expected at least 6 reviews';
    ASSERT cnt_registrations >= 5, 'Expected at least 5 registrations';
END;
$$;
